        "# Function to test stationarity\n",
        "test_stationarity <- function(time_series) {\n",
        "  # Augmented Dickey-Fuller test\n",
        "  adf_test <- adf.test(time_series)\n",
        "\n",
        "  # KPSS test\n",
        "  kpss_test <- kpss.test(time_series)\n",
        "\n",
        "  # Return results\n",
        "  results <- list(\n",
        "    adf = list(\n",
        "      statistic = adf_test$statistic,\n",
        "      p_value = adf_test$p.value,\n",
        "      is_stationary = adf_test$p.value < 0.05\n",
        "    ),\n",
        "    kpss = list(\n",
        "      statistic = kpss_test$statistic,\n",
        "      p_value = kpss_test$p.value,\n",
        "      is_stationary = kpss_test$p.value >= 0.05\n",
        "    )\n",
        "  )\n",
        "\n",
        "  # Overall conclusion\n",
        "  results$conclusion <- ifelse(\n",
        "    results$adf$is_stationary && results$kpss$is_stationary,\n",
        "    \"Series is stationary\",\n",
        "    \"Series may not be stationary\"\n",
        "  )\n",
        "\n",
        "  return(results)\n",
        "}\n",
