        "# Function to calculate additional performance metrics\n",
        "calculate_performance_metrics <- function(forecast_obj, test_data) {\n",
        "  # Get point forecasts\n",
        "  point_forecasts <- forecast_obj$mean\n",
        "\n",
        "  # Calculate forecasting errors\n",
        "  errors <- test_data - point_forecasts\n",
        "\n",
        "  # Calculate additional metrics\n",
        "  metrics <- list(\n",
        "    # Basic metrics (already in accuracy function but included for completeness)\n",
        "    ME = mean(errors, na.rm = TRUE),\n",
        "    RMSE = sqrt(mean(errors^2, na.rm = TRUE)),\n",
        "    MAE = mean(abs(errors), na.rm = TRUE),\n",
        "\n",
        "    # Additional metrics\n",
        "    MASE = mean(abs(errors)) / mean(abs(diff(test_data))),\n",
        "    R_squared = 1 - sum(errors^2) / sum((test_data - mean(test_data))^2),\n",
        "\n",
        "    # Direction accuracy - percentage of times forecast correctly predicts up/down movement\n",
        "    DA = mean(sign(diff(c(NA, point_forecasts))) == sign(diff(c(NA, test_data))), na.rm = TRUE)\n",
        "  )\n",
        "\n",
        "  return(metrics)\n",
        "}\n",
